import { goals } from "../db/schema"
import { db } from "../db/schema/dbconnect"
import type { CreateGoalRequest } from "../interfaces/interfaces"

export async function createGoal({
  title,
  desiredWeeklyFrequency,
}: CreateGoalRequest) {
  const result = await db
    .insert(goals)
    .values({
      title,
      desiredWeeklyFrequency,
    })
    .returning()

  const goal = result[0]

  return {
    goal,
  }
}
